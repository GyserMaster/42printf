#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*buffer;
	size_t	i;

	buffer = malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!(buffer))
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		buffer[i] = s[i];
		i++;
	}
	buffer[i] = '\0';
	return (buffer);
}
