void	ft_putdouble_fd(double n)
{
	if (n == (double)0)
	{
		return ;
	}
	
	if (n != 0)
	{
		ft_putdouble_fd(n / 10);
		ft_putnbr_fd((int)n % 10, 1);
	}
}